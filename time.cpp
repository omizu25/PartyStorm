//=============================================================================
// 位置のセッター
// Author : 唐﨑結斗
// 概要 : 位置のメンバ変数に引数を代入
//=============================================================================
void CTime::SetPos(const D3DXVECTOR3 &pos)
{
	m_pos = pos;

	m_pObject2D->SetPos(m_pos);
	m_pScore->SetPos(m_pos);
}

//=============================================================================
// 向きのセッター
// Author : 唐﨑結斗
// 概要 : 向きのメンバ変数に引数を代入
//=============================================================================
void CTime::SetRot(const D3DXVECTOR3 &rot)
{
	m_rot = rot;

	m_pObject2D->SetRot(m_rot);
	m_pScore->SetRot(m_rot);
}

//=============================================================================
// 大きさのセッター
// Author : 唐﨑結斗
// 概要 : 大きさのメンバ変数に引数を代入
//=============================================================================
void CTime::SetSize(const D3DXVECTOR3 & size)
{
	m_size = size;

	m_pObject2D->SetSize(m_size);
	m_pScore->SetWholeSize(D3DXVECTOR3(m_size.x, size.y / 3.0f, size.z));
	m_pScore->SetSize(D3DXVECTOR3(m_size.x / 3.0f, size.y / 3.0f, size.z));
}

//=============================================================================
// 大きさのセッター
// Author : 唐﨑結斗
// 概要 : 大きさのメンバ変数に引数を代入
//=============================================================================
void CTime::SetTime(const int nTime)
{// 時間の設定
	m_nTime = nTime;

	// スコアの設定
	m_pScore->SetScore(m_nTime);
	m_pScore->AddScore(m_nTime);
}
